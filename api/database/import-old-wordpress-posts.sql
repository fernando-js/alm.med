<h2 class="wp-block-heading"></h2>



<p>A avaliação pré anestésica (APA), levando-se ao pé da letra, consiste no conhecimento clínico do paciente visando sua avaliação anestésica para cirurgia. Mas, ela tem uma função mais abrangente e essencial do que apenas esta.</p>



<p>Compreende o primeiro contato do paciente com seu médico anestesista onde irá confidencializar toda sua trajetória clínica, uso de medicamentos, histórico de procedimentos prévios, alergias medicamentosas e alimentícias, medos, dúvidas, problemas de saúde entre outros que os queira.</p>



<h2 class="wp-block-heading">Função da avaliação pré-anestésica</h2>



<p>Essa avaliação, ajuda imensamente a diminuir os riscos clínicos de desfechos desfavoráveis voltados a cirurgia. Orientando e ajudando o anestesista a identificar, tratar e criar planos de manejo clínico intra operatório evitando alterações do estado físico-clínico do paciente, uso desnecessário de medicamentos e técnicas anestésicas e analgesia, visando o bem estar e segurança do paciente para que o procedimento cirúrgico corra da melhor e mais segura maneira possível evitando-se intercorrências inesperadas.</p>



<h2 class="wp-block-heading">Quem deve avaliar?</h2>



<p>Segundo a sociedade brasileira de anestesiologia, desde 2006, pela resolução do cfm 1802/06 (Art. 1o a) o médico anestesista deve ser o responsável pela APA.</p>



<h2 class="wp-block-heading">Avaliação pré-anestésica: quando deve ser realizada?</h2>



<p>A APA deve ser realizada para qualquer procedimento cirúrgico, desde os menores até os maiores e mais complexos procedimentos. Obviamente com critérios, solicitação de exames e foco nas cirurgias de maior complexidade e tempo cirúrgico.</p>



<h2 class="wp-block-heading">Entrevista inicial</h2>



<p>Neste questionário, o médico anestesiologista entrevistará o paciente para solicitar sobre suas condições físicas, psicológicas, bem como conhecer todas as informações de interesse clínico sobre o paciente:</p>



<ul class="wp-block-list">
<li>Existência de doenças pré-existentes;</li>



<li>Portador de alguma alergia;</li>



<li>Uso de medicamentos e obtenção de informações do nome e dose diária;</li>



<li>História pregressa de alguma cirurgia onde foi necessária a utilização de anestesia;</li>



<li>Cirurgia (ou procedimento) ao qual será submetido.</li>
</ul>



<h2 class="wp-block-heading">Cuidados necessários durante da consulta pré-anestésica</h2>



<p>Durante a consulta pré-anestésica o anestesiologista informará o paciente sobre todos os procedimentos e cuidados que deverão ser tomados antes, durante e depois da realização do procedimento cirúrgico, as possíveis complicações e efeitos indesejáveis que possam ocorrer.</p>



<p>Essas informações compreendem:</p>



<ul class="wp-block-list">
<li>Período de jejum pré-operatório;</li>



<li>Técnica e escolha da anestesia que o paciente irá ser submetido para sua cirurgia, assim como a orientação e explicação da analgesia pós operatória;</li>



<li>Prescrições referente as medicações habituais que o paciente deverá receber ou evitar tomar antes ou no dia cirúrgico (ex: anti-hipertensivos, hipoglicemiantes orais, anticoagulantes, analgésicos);</li>



<li>Orientações de medicamentos ansiolíticos (calmante) na véspera da cirurgia visando não provocar sintomas de ansiedade, insônia prévia à cirurgia;</li>



<li>Informações sobre possíveis complicações ou efeitos indesejáveis pós anestésico como tremores, dores, náuseas e vômitos entre outros;</li>



<li>Solicitação para retirada de próteses dentárias móveis como: dentaduras, pivôs e pontes;</li>



<li>Recomendação para, no dia da cirurgia, cosméticos (batom, base, esmalte, pintura facial, aplique de cabelo) ou acessórios (óculos, lentes de contato, brincos, colares, anéis, relógios, pulseiras etc.) não serem utilizados;</li>



<li>Proibição do consumo de bebidas alcoólicas e tabagismo, por pelo menos 24 horas antes da intervenção cirúrgica.</li>
</ul>



<h3 class="wp-block-heading"><strong>&nbsp;Orientação sobre o Jejum pré operatório</strong></h3>



<p>O cumprimento do jejum é de extrema importância, pois caso o paciente não respeite as orientações e vá para cirurgia de estômago cheio, no momento que se induz a anestesia, esse perde seus reflexos de deglutição e proteção das suas vias aéreas.</p>



<p>Isso pode provocar regurgitação com aspiração de conteúdo gástrico para os pulmões levando a uma pneumonia química aspirativa gravíssima com sérias complicações.</p>



