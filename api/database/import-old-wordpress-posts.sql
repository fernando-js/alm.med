<h3 class="wp-block-heading">4 Avaliação endocrinológica</h3>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pela alimentação e estilo de vida, muitos paciente sofrem de doenças endócrinas como por exemplo diabetes mellitus, problemas tireoidianos, dislipidemias, fazendo com que muitos realizem terapêutica medicamentosa.</p>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; O anestesista irá questionar, orientar, prescrever ou proscrever quanto ao uso de hipoglicemiantes orais, medicamentos para hipo ou hipertireoidismo, dislipidemia que poderão ou não serem administrados previamente a cirurgia.</p>



<h3 class="wp-block-heading">5- Avaliação e solicitação de exames pré operatórios</h3>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Os pacientes assintomáticos ASA 1 com idade até 40 anos não se beneficiam com a realização de exames laboratoriais (9) exceto em cirurgias de alta complexidade ou longa duração, uma vez que apresentam estado de saúde satisfatório com poucos fatores de risco para complicações.</p>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Os demais pacientes deverão ter sua avaliação criteriosa com relação a sua patologia de base, reservas clínicas, medicamentos em uso, porte cirúrgico e riscos operatórios. Para cada tipo de patologia de base, a investigação clínica deverá ser mais minuciosa. Abaixo listo alguns dos principais exames pré operatórios solicitados recomendados pela sociedade brasileira de anestesiologia.</p>



<h3 class="wp-block-heading"><strong>ECG</strong></h3>



<ul class="wp-block-list">
<li>Pode ser solicitado em pacientes com idade superior ou igual a 40 anos e obrigatoriamente acima de 50 anos;</li>



<li>Paciente com doença coronariana, arritmia, doença arterial periférica ou cerebrovascular, doença cardíaca estrutural, hipertensão arterial sistêmica e diabetes;</li>



<li>Não é necessário para pacientes de rotina assintomáticos e/ou que se submeterão a procedimentos de baixo risco.</li>
</ul>



<h3 class="wp-block-heading"><strong>Hemograma</strong></h3>



<ul class="wp-block-list">
<li>Extremos de idade, menores de 1 ano e idade > 50 anos</li>



<li>Procedimentos invasivos (cirurgias de longa duração, endometriose profunda e mastectomia com reconstrução);</li>



<li>Comorbidades como HAS, IAM prévio, IAVC prévio entre outros;</li>



<li>História de anemia, sangramento ou outra desordem hematológica</li>
</ul>



<h3 class="wp-block-heading"><strong>Coagulograma</strong></h3>



<ul class="wp-block-list">
<li>Coagulopatias ou pacientes em uso de anticoagulantes;</li>



<li>Doença renal ou hepática;</li>



<li>Cirurgia de Grande porte ou invasiva de alto risco de sangramento.</li>
</ul>



<h3 class="wp-block-heading"><strong>Função Renal (uréia e Creatinina)</strong></h3>



<ul class="wp-block-list">
<li>Pode ser solicitado em pacientes com idade superior ou igual a 40 anos e obrigatoriamente acima de 50 anos;</li>



<li>Portadores de HAS, endocrinopatias (como o DM), nefropatia, insuficiência hepática; ou cardíaca, transplantados renais ou uso de drogas nefrotóxicas.</li>
</ul>



<h3 class="wp-block-heading"><strong>Eletrólitos (sódio, potássio, cálcio)</strong></h3>



<ul class="wp-block-list">
<li>Pacientes em uso de diurético;</li>



<li>Portadores de HAS, Endocrinopatias (como o DM), nefropatia, insuficiência hepática ou cardíaca.</li>



