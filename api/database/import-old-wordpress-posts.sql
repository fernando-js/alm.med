<h3 class="wp-block-heading"><strong>Equação de Cockcroft-Gault</strong></h3>



<p>A equação de Cockcroft-Gault [(140-idade) x peso/ (72 x creatinina) x 0,85 (se mulher)], em sua publicação original, no ano de 1973, foi amplamente utilizada para estimar a depuração da creatinina sérica. Ela foi desen­volvida a partir de resultados de 249 homens caucasianos hospitalizados, com idade média de 57 anos (faixa etária de 18 a 92 anos) e com função renal normal. Não foi padro­nizada para uma área de superfície corporal de 1,73 m2&nbsp;e por isso foi criado o fator de correção 0,85 para mulheres e o seu resultado é expresso em mL/min. É uma equação que superestima a TFG, porque não considera a secreção tubular da creatinina, o aumento do peso em pessoas obesas e a sobrecarga de fluidos.<sup>(15,20,21)</sup></p>



<p>A fórmula de Cockcroft-Gault considera a existência de uma relação inversa entre a idade e a excreção diária de creatinina urinária. Sabe-se que esta excreção diária de creatinina (por kg de peso corporal) diminui com a progressão da idade devido a uma redução também progressiva na massa muscular (expressa em percentual em relação ao peso corporal). Também na obesidade, em que um aumento do tecido adiposo determina uma redução na porcentagem de massa muscular relacionada ao peso corporal, ocorre uma menor excreção diária de creatinina por kg de peso corporal quando se compara com indivíduos com peso normal. Por sua vez, a fórmula de CG considera a redução na excreção de creatinina urinária relacionada à idade, mas não a relaciona com a excreção relativamente menor de creatinina em função da obesidade.<sup>(20)</sup></p>



<p>Alguns autores sugerem que a estimativa da função renal por esta fórmula seja mais fidedigna do que a medida do&nbsp;<em>clearance</em>&nbsp;de creatinina devido à suscetibilidade deste último método a erros. Apesar de ser uma das equações mais utilizadas para estimar a TFG, ela não tem uma boa acurácia. Por exemplo, em um estudo com 252 pessoas que realizaram medida do&nbsp;<em>clearance</em>&nbsp;de creatinina com coleta de urina de 24 horas, verificou-se que aproxima­damente 1/3 delas apresentou resultados não confiáveis por problemas relacionados com a coleta da urina ou com os dados fornecidos.<sup>(20,22,23)</sup></p>



<p>Esta equação apresenta inúmeras limitações, dentre elas temos: a TFG é estimada, mas não corrigida, para a área de superfície corpórea de 1,73 m<sup>2</sup>, ao contrário de outras equações. Tal como acontece com a equação Schwartz, também a Cockcroft-Gault foi desenvolvida utilizando um ensaio de Jaffe desatualizado para dosagem de creatinina, o qual deve ser reavaliado quando calculado com métodos de creatinina calibrados. Por fim, requer o peso corporal, o que pode não estar sempre disponível, especialmente em&nbsp;<em>software&nbsp;</em>de laboratório que fornece cálculos de rotina a partir de medições de creatinina sérica.<sup>(1,6)</sup></p>



<h3 class="wp-block-heading"><strong>MDRD</strong></h3>



<p>A equação MDRD para estimativa da taxa de filtração glomerular (TFG) foi originalmente desenvolvida com base nos dados do estudo&nbsp;<em>Modification of Diet in Renal Disease</em>&nbsp;(MDRD) em pacientes com DRC e não incluiu indivíduos saudáveis. O desenvolvimento da equação MDRD foi baseado no&nbsp;<em>clearance</em>&nbsp;de iotalamato-I125, considerado padrão ouro, portanto, ela estima a TFG (em mL/min/1,73m<sup>2</sup>) e não a depuração de creatinina. O coorte para desenvolvimento dessa equação abrangeu 88% de indivíduos brancos com DRC, não transplantados, inclu­indo pacientes com nefropatia diabética e média de idade de 51 anos e TFG de 40 mL/min/1,73 m<sup>2</sup>. Foi publicada em 1999 e posteriormente simplificada por outras equa­ções mais comumente usadas. Ela estima a TFG usando a creatinina sérica, idade, raça e gênero a fim de observar as diferenças causadas pela massa muscular. A MDRD pode estimar melhor a função renal em pacientes idosos, mostrando uma predição confiável da TFG nessa popu­lação.<sup>(20,21,24)</sup></p>



<p>Em sua versão original, a equação MDRD era calcu­lada com as determinações de albumina e ureia nitro­genada séricas. Porém, uma fórmula do MDRD abreviada com “quatro variáveis” tem sido recomendada, devido a seu desempenho ser tão bom quanto a equação inicial. A TFG calculada com a equação do MDRD e a TFG real são muito próximas para resultados &lt;60 mL/min/1,73m<sup>2</sup>,&nbsp;enquanto que a TFG excede a taxa estimada por um valor pequeno quando a TFG é &gt;60 mL/min/1,73m<sup>2</sup>.<sup>(20,25)</sup></p>



<p>A precisão e a acurácia do MDRD são reduzidas quando a TFG aumenta e em diferentes grupos étnicos. Outras nacionalidades estão validando essa equação, o que tem facilitado as comparações internacionais, como, por exemplo, quando a DRC é definida apenas pela TFG &lt;60 mL/min/1,73m<sup>2</sup>, a prevalência é de 2,5% a 11,2% na população adulta da Europa, Ásia, América do Norte e Austrália. O cálculo pelo MDRD, como inclui inúmeras variáveis, são muito complexos para a rotina do laboratório e requerem um relativo conhecimento de matemática ou um programa de computação capaz de realizá-lo.<sup>(9,11)</sup></p>



<p>Existem tabelas desenvolvidas, uma para mulheres e outra para homens, que permitem aos profissionais de saúde estimar a TFG com a concentração de creatinina sérica e a idade do paciente. Essas tabelas foram base­adas na fórmula do estudo MDRD de quatro variáveis, na qual a componente etnia negra (importante para estimar a TFG na população negra dos EUA, mas não na população brasileira) foi excluída. Essas tabelas mostram os valores de TFG correspondentes a valores específicos de creati­nina sérica entre 0,5-5,0 mg/dL e nas faixas etárias de 18 a 80 anos.<sup>(20)</sup></p>



<h3 class="wp-block-heading"><strong>CKD/EPI</strong></h3>



<p>O grupo&nbsp;<em>Chronic Kidney Disease Epidemiology Collaboration</em>&nbsp;(CKD-EPI) desenvolveu, em 2009, uma nova equação, que é uma variação da fórmula do MDRD, a partir de um estudo de coorte, que incluiu indivíduos com e sem DRC. A equação CKD-EPI usa as mesmas quatro variáveis que a equação do MDRD, mas, comparati­va­mente, apresenta melhor desempenho e previsão de risco, além de apresentar menor viés e uma maior acurácia, principalmente nas faixas de TFG &gt;60 mL/min/1,73m<sup>2</sup>, do que o estudo MDRD para uso clínico de rotina.<sup>(20,26-28)</sup></p>



<p>Essa equação foi desenvolvida e validada com conjuntos de dados de dez estudos, em um total de 8.254 participantes. Outros 16 estudos com 3.896 participantes foram utilizados para validação externa, dos quais, 72% (n= 2.810) vieram de populações de alto risco. Ao ser com­parada com o MDRD, o CKD-EPI mostrou precisão seme­lhante no subgrupo de pacientes com TFG &lt;60 mL/min/1,73m<sup>2</sup>. O CKD-EPI está sendo considerado, pela comunidade de nefrologia, o melhor padrão para estimar a função renal. Porém, pode superestimar TFG em pacientes que estão extremamente abaixo do peso e sub­estimar em pacientes obesos mórbidos e diabéticos.<sup>(26,29,30)</sup></p>



<p>As fórmulas para avaliação da TFG estão disponíveis em programas para&nbsp;<em>palmtops</em>, computadores e&nbsp;<em>i-phones</em>&nbsp;e estão amplamente disseminadas na internet (por exem­plo, em&nbsp;<em>websites</em>&nbsp;da Sociedade Brasileira de Nefrologia e da&nbsp;<em>National Kidney Foundation</em>). No entanto, a maioria dos profissionais que trabalham em clínicas de atenção primária à saúde ainda continua sem acesso imediato a esses equipamentos de computação e então calculam a TFG manualmente. Esse processo é demorado, pode atrasar o diagnóstico e o encaminhamento para o nefro­logista sendo também mais sujeito a erros.<sup>(20)</sup></p>



<p>A equação denominada CKD/EPI usa as mesmas quatro variáveis que a equação do MDRD, mas, compa­rativamente, apresenta melhor desempenho e previsão de desfechos adversos. As observações de menor viés e maior acurácia e precisão da equação CKD/EPI em comparação à equação do estudo MDRD, particular­mente nas faixas de TFG &gt; 60 mL/min/1,73 m<sup>2</sup>, cons­tituem a razão para preconizar o seu uso clínico em substituição às equações de estimativa da TFG até então utilizadas. Com base em dados da NHANES (<em>National Health and Nutrition Examination Survey</em>), a TFG média estimada foi de 94,5 mL/min por 1,73 m<sup>2</sup>&nbsp;<em>versus&nbsp;</em>85,0 mL/min por 1,73 m<sup>2</sup>, e da prevalência da doença renal crônica foi de 11,5% contra 13,1%, respectivamente.<sup>(28)</sup></p>



<p>A equação CKD-EPI é expressa como uma equação simples: TFG = 141 X min (SCR/k, 1) a X max (SCR/k, 1) -1,209 X 0,993 Idade X 1,018 [se mulher] X 1,159 [negro]. Onde SCR corresponde a creatinina sérica (mg/dL), k é de 0,7 e 0,9 para mulheres e homens respectivamente, a é -0,329 e -0,411 para mulheres e homens respecti­va­mente, min indica o mínimo de SCR/k ou 1, e max indica o máximo de SCR/k ou 1.<sup>(26)</sup></p>



<p>Os estudos originais utilizados para a equação CKD/EPI foram divididos em dois grupos: populações de alto risco, como pacientes com DRC clínica, com uma média de TFG &lt;90 ml/min/1,73 m<sup>2</sup>&nbsp;e populações de baixo risco, como potenciais doadores de rins, caracterizada por uma média de TFG &gt;90 ml/min por 1,73 m<sup>2</sup>. Não é de se estranhar que a equação CKD/EPI apresente um melhor desempenho na amostra de validação externa do que a equação MDRD, feita utilizando apenas uma população de alto risco; no entanto, essa validação não isenta o problema subjacente com o desempenho de estimar a TFG em diferentes populações. Os autores da equação CKD/EPI reconheceram isso, afirmando que “uma única equação é improvável que funcione igualmente bem em todas as populações”.<sup>(26)</sup></p>



<p>Estudos mostram que a equação CKD/EPI é mais precisa do que as outras duas equações, que estimam a taxa de TFG de uso comum. Esta conclusão não é real­mente pertinente para a prática clínica, porque os paci­entes apresentam histórias clínicas, fatores de risco, tes­tes laboratoriais e achados de exame que informam o risco para DRC. A diminuição normal da TFG com o enve­lhecimento não significa lesão crônica subjacente do parên­quima renal em biópsia. Após o diagnóstico da DRC pode-se escolher entre a equação CKD/EPI, para moni­torar a doença, porém há uma perda de precisão quando se inclu­em pacientes de baixo risco, ou a equa­ção MDRD, que perde um pouco de precisão nos métodos estatísticos.<sup>(22)</sup></p>



<h3 class="wp-block-heading"><strong>Equações que estimam a TFG baseadas na cistatina</strong></h3>



<p>A cistatina C é uma proteína básica não glicosilada que faz parte da superfamília de cisteínas inibidoras de proteases. Tem um baixo peso molecular de 13 kDa e é produzida apenas por todas as células nucleadas a uma velocidade estável. Foi descoberta pela primeira vez, em 1985, como um marcador endógeno promissor para avaliar a TFG. É livremente filtrada no glomérulo e, em seguida, é reabsorvida e metabolizada sem sofrer secreção no túbulo proximal.<sup>(14,20)</sup></p>



<p>Consequentemente, embora a cistatina C seja filtrada pelos glomérulos, seu&nbsp;<em>clearance&nbsp;</em>urinário não pode ser medido, o que torna o estudo dos fatores que afetam seu&nbsp;<em>clearance</em>&nbsp;e geração difíceis de realizar. Além disso, há evidência preliminar de que as concentrações séricas de cistatina C são influenciadas pelo uso de corticosteroides e estão relacionadas à idade, sexo, peso, altura, taba­gismo e nível de proteína C reativa, mesmo após o ajuste para a depuração da creatinina. No momento, o papel clínico para medir a concentração da cistatina C não foi elucidado, mas ainda existe a possibilidade de que ela venha a ser um marcador útil de disfunção renal precoce, como parte de programas de rastreamento. Devido ao fato de a cistatina C não depender da massa muscular, ela parece ser mais sensível do que a equação do estudo MDRD no diagnóstico precoce da DRC, principalmente na população de idosos. Além disso, a cistatina C pode ter um papel na previsão de pacientes com DRC com maior risco de complicações.<sup>(20)</sup></p>



<p>A cistatina C é considerada um potencial substituto da creatinina sérica como marcador de filtração glomerular. Estudos mostraram que esse marcador é tão relacionado com a filtração glomerular quanto a creatinina sérica. Estudo em crianças, no início da insuficiência renal, mos­trou que, das inúmeras equações baseadas na cistatina C para avaliar a TFG, algumas apresentaram melhor acurácia e precisão em mostrar uma diminuição da TFG que as equações baseadas na creatinina. Outras apre­sentam similaridade entre as equações que usam os dois analitos. Também foi observado que as concentrações séricas de creatinina e cistatina C apresentam correlação forte e estatisticamente significativa.<sup>(8,31)</sup></p>



