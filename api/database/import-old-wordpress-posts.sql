<h2 class="wp-block-heading"><strong>Para que serve a pré-anestesia?</strong></h2>



<p>A avaliação pré-anestésica melhora a qualidade do ato anestésico-cirúrgico e reduz o tempo de internação e as chances de uma cirurgia ser cancelada.&nbsp;</p>



<p>Além disso, estudos apontam que a pré-anestesia contribui também com a redução dos índices de mortalidade em procedimentos que usam a anestesia, além de uma redução no tempo de internação, liberando o paciente mais rapidamente para que ele volte às suas funções.</p>



<p>O momento da pré-anestesia é ideal para que o paciente e seus acompanhantes façam perguntas e sanem suas dúvidas em relação à anestesia, ajudando a trazer mais confiança e segurança na realização do procedimento e diminuindo os níveis de ansiedade, que são muito comuns em procedimentos que envolvem o uso de anestesia geral, por exemplo.</p>



<h2 class="wp-block-heading"><strong>Qual a importância da pré-anestesia?</strong></h2>



<p>A avaliação ou consulta pré-anestésica é extremamente importante na preparação do paciente. Nela, o anestesiologista verifica a condição física e mental do paciente, orienta sobre o pré-operatório e decide qual é o tipo de anestesia necessário.&nbsp;</p>



<h2 class="wp-block-heading"><strong>O que acontece na pré-anestesia?</strong></h2>



<p>Na pré-anestesia, o médico anestesiologista pergunta ao paciente ou seu acompanhante sobre o uso de medicamentos, alergias e outros problemas de saúde. Também avalia a capacidade funcional e as vias respiratórias&nbsp;do paciente (especialmente nos casos de anestesia geral)&nbsp;e pode solicitar exames complementares, se necessário.&nbsp;</p>



<p>O momento de avaliação antes da anestesia é obrigatório e deve ser marcado previamente, no caso de cirurgias agendadas. Para pacientes hospitalizados, com cirurgia marcada, a avaliação é feita no próprio hospital.&nbsp;</p>



<p>No geral, a pré-anestesia acontece nos procedimentos que são eletivos ou que acontecem nos casos de urgência.&nbsp;</p>



<p>Nos casos de emergência médica, onde o paciente pode estar inconsciente e o procedimento precisa ser feito para salvar sua vida, a anestesia é realizada mesmo sem a conversa de pré-anestesia, considerando elevados padrões de segurança e indicadores internacionais.</p>



<h2 class="wp-block-heading"><strong>Quem deve fazer a pré-anestesia do paciente?</strong></h2>



<p>Geralmente, a conversa de pré-anestesia é feita com médico anestesiologista que participará de seu procedimento cirúrgico ou não.</p>



<h2 class="wp-block-heading"><strong>Como é a pré-anestesia no trabalho de parto?</strong></h2>



<p>As gestantes devem passar por avaliação pré-anestésica entre a 34º e a 36ª semana de gestação, mesmo que optem pelo parto vaginal. A medida é preventiva, caso seja necessário recorrer a uma cesariana devido a problemas no trabalho de parto.&nbsp;</p>



