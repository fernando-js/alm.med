<h2 class="wp-block-heading">Classificação dos Anestésicos Locais :</h2>



<p>Os anestésicos locais são divididos em dois grandes grupos, os ésteres e as amidas, ou seja, são constituídos molecularmente por um anel benzeno e uma amina terciária em uma ligação éster ou amida.</p>



<p>Os ésteres são degradados rapidamente pela colinesterase plasmática, enquanto os amidas pelos microssomos hepáticos. A forma a qual são degradados determina a duração e o efeito de cada classe, sendo os ésteres de duração rápida e os amidas de duração mais prolongada. Outra diferença entre eles é a capacidade de gerar reações alérgicas. Os ésteres são mais alérgenos que as amidas.</p>



