<h2 class="wp-block-heading">Avaliação pré-anestésica: avaliações do paciente</h2>



<p>Após realizado a anamnese e coleta da história clínica-cirúrgica do paciente, são feitas as diversas avaliações do paciente como:</p>



<ul class="wp-block-list">
<li>Vias Aéreas;</li>



<li>Respiratórias;</li>



<li>Cardiovasculares;</li>



<li>Endocrinológicas;</li>



<li>Solicitações de exames pré operatórios caso haja necessidade. </li>
</ul>



<h3 class="wp-block-heading">1- Avaliação e classificação das vias aéreas.</h3>



<p>Nessa avaliação são observados diversos fatores de classificação e prognósticos de risco para ventilação e intubação.</p>



<p>Os preditores de ventilação difícil manual bolsa-máscara são:</p>



<ul class="wp-block-list">
<li>Presença de barba, cavanhaque ou bigode por permitir o vazamento de ar pelo não acoplamento adequado da máscara ao rosto;</li>



<li>História de ronco ou apnéia do sono;</li>
</ul>



<ul class="wp-block-list">
<li>Classificação de Mallampati 3 e 4;</li>



<li>Abertura bucal pequena (distância interincisivos) &lt; 3 cm;</li>
</ul>



<ul class="wp-block-list">
<li>Condição dentária precária ou ausência de dentes;</li>



<li>Índice de massa corpórea >26 Kg/m2;</li>
</ul>



<p>Os preditores de intubação difícil são:</p>



<ul class="wp-block-list">
<li>Classificação de Mallampati 3 e 4;</li>



<li>Abertura bucal pequena (distância interincisivos) &lt; 3 cm</li>



<li>Distância tireo-mento &lt; 6 cm</li>



<li>Distância esterno-mento &lt; 12,5 cm</li>



<li>Limitação de flexão &lt; 35* e extensão &lt; 80* do pescoço;</li>



<li>Retrognatismo ou prognatismo da mandíbula;</li>



<li>Dentes grande ou proeminentes</li>
</ul>



<h4 class="wp-block-heading">Classificação de Mallampati</h4>



<p>Utilizada para verificar o grau de visualização das estruturas da orofaringe sendo dividida em 4 classes:</p>



<ol class="wp-block-list">
<li> Classe I – palato mole, fauce, úvula e pilares amigdalianos visíveis;</li>



<li> Classe II – palato mole, fauce e úvula visíveis;</li>



<li> Classe III – palato mole e base da úvula visíveis;</li>



<li> Classe IV – palato mole totalmente não visível.</li>
</ol>



<h3 class="wp-block-heading"><br>2- Avaliação respiratória</h3>



<p>Nessa avaliação são retiradas todas as características e histórias pulmonares do paciente como:</p>



<ul class="wp-block-list">
<li>Tabagismo e sua carga tabágica;</li>



<li>História de asma, bronquite, sinusite ou outra complicação respiratória assim como uso de medicações para seu controle;</li>



<li>História de DPOC, enfisema, fibrose cística ou alguma outra patologia pulmonar;</li>



<li>Cirurgia torácica prévia.</li>
</ul>



<p>Caso o anestesista identifique alguma alteração significativa ou que irá interferir no andamento e segurança intra operatória, ele poderá solicitar exames adicionais, introduzir ou readequar as doses de medicamentos com objetivo de otimizar e estabilizar o quadro pulmonar. Deixando o paciente na sua melhor forma e estado clínico-cirúrgico, minimizando riscos perioperatórios.</p>



<h3 class="wp-block-heading">3- Avaliação cardiovascular</h3>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Os eventos cardiovasculares são as principais causas de mortalidade perioperatória em cirurgias não cardíacas. Comorbidades como hipertensão arterial, eventos isquêmicos ou obstrutivos prévios&nbsp; devem ser alvos de uma avaliação minuciosa do profissional, que poderá realizar ajustes terapêuticos ou comportamentais. Vale ressaltar o risco associado ao uso de anticoagulantes os quais elevam a chance de sangramentos cirúrgicos.</p>



<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Para estimar o risco cardiovascular, foram criados diversos algoritmos de avaliação cardiológica pré-operatória, como o índice de risco cardíaco descrito por Goldman et al., em 1977 (6), o primeiro modelo multifatorial específico para complicações cardíacas perioperatórias largamente usados.</p>



<p>Esse índice contempla variáveis referentes à avaliação clínica, eletrocardiograma e o tipo de cirurgia (intra-abdominal, intratorácica, aórtica ou de emergência), conferindo pontuações com intuito de estratificar o paciente nas classes I a IV quanto ao risco de apresentar complicações cardiovasculares ou evoluir para óbito.</p>



